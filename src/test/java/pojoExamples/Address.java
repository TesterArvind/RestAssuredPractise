package pojoExamples;

public class Address {
	
	private String cityName;
	private String gullyNo;
	private String mohalla;
	
	public String getCityName() {
		return cityName;
	}
	
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getGullyNo() {
		return gullyNo;
	}
	public void setGullyNo(String gullyNo) {
		this.gullyNo = gullyNo;
	}
	public String getMohalla() {
		return mohalla;
	}
	public void setMohalla(String mohalla) {
		this.mohalla = mohalla;
	}

}
